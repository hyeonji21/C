#include <stdio.h>

int main(void)
{
	char a;
	a = '!';
	printf("%c", a);

	char b;
	b = '@';
	printf("%c", b);

	char c;
	c = '#';
	printf("%c", c);

	char d;
	d = '$';
	printf("%c", d);

	char e;
	e = '%';
	printf("%c", e);

	char f;
	f = '^';
	printf("%c", f);

	char g;
	g = '&';
	printf("%c", g);

	char h;
	h = '*';
	printf("%c", h);

	char i;
	i = '(';
	printf("%c", i);

	char j;
	j = ')';
	printf("%c", j);

	return 0;
}